package packages.technics;

public class Passport {
    public String name;
    public String model;
    public String label;
    public String color;
    public String type;

    public Passport(String name, String model, String label, String color, String type) {
        this.name = name;
        this.model = model;
        this.label = label;
        this.color = color;
        this.type = type;
    }
}
